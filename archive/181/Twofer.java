class Twofer {
    String twofer(String name) {
        if (name == "Alice") {
            return "One for Alice, one for me.";
        } else if (name == "Bob") {
            return "One for Bob, one for me.";
        } else if (name == "Zaphod") {
            return "One for Zaphod, one for me.";
        } else return "One for you, one for me.";       
        
    }
}
